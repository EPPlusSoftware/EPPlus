using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Text.RegularExpressions;

namespace OfficeOpenXml.FormulaParsing.Excel.Functions.DateAndTime
{
    internal class TimeStringParserV2
    {

        public virtual double Parse(string input)
        {
            var str = new TimeString(input);
            if(str.IsValidFormat)
            {
                return str.SerialNumber;
            }
            return double.NaN;
        }
    }
}
