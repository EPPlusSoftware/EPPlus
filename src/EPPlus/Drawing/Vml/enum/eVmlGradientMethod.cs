namespace OfficeOpenXml.Drawing.Vml
{
    /// <summary>
    /// The fill method used in a gradient fill
    /// </summary>
    public enum eVmlGradientMethod
    {
        /// <summary>
        /// No sigma fill.
        /// </summary>
        None,
        /// <summary>
        /// Linear fill.
        /// </summary>
        Linear,
        /// <summary>
        /// Linear sigma fill.
        /// </summary>
        LinearSigma,
        /// <summary>
        /// Sigma fill. Default.
        /// </summary>
        Sigma,
        /// <summary>
        /// Any sigma fill.
        /// </summary>
        Any
    }
}
